package aulaDia21;

public class Carro {
	String cor;
	int porta;
	int rodas;
	String tipo;
	
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public int getPorta() {
		return porta;
	}
	public void setPorta(int porta) {
		this.porta = porta;
	}
	public int setRodas() {
		return rodas;
	}
	public void setRodas(int rodas) {
		this.rodas = rodas;
	}
	public String getTipo() {
		return tipo;
	}
	public void setTipo(String Tipo) {
		this.tipo = tipo;
	}

}
